import { spawnSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { delimiter, resolve } from "node:path";
import prettier from "prettier";

const root = resolve(import.meta.dirname, "..");
const packageJson = JSON.parse(readFileSync(resolve(root, "package.json"), "utf8"));
const schema = JSON.parse(readFileSync(resolve(root, "protocol", "data-explorer.v2.schema.json"), "utf8"));
const outputPath = resolve(root, "docs", "reference.md");
const operations = loadOperationCatalog();

const output = await prettier.format(
  `${[
    "# Generated interface reference",
    "",
    "> Generated from `package.json`, `python/data_wrangler_runtime/operations.py`, and `protocol/data-explorer.v2.schema.json`. Do not edit this file by hand; run `npm run generate:reference`.",
    "",
    "## Commands",
    "",
    table(
      ["Command", "Title"],
      packageJson.contributes.commands.map((command) => [code(command.command), escapeCell(command.title)])
    ),
    "",
    "## Settings",
    "",
    table(
      ["Setting", "Type", "Default", "Allowed values", "Description"],
      Object.entries(packageJson.contributes.configuration.properties).map(([name, setting]) => [
        code(name),
        code(Array.isArray(setting.type) ? setting.type.join(" | ") : setting.type),
        code(JSON.stringify(setting.default)),
        setting.enum ? setting.enum.map((value) => code(JSON.stringify(value))).join(", ") : "—",
        escapeCell(setting.description)
      ])
    ),
    "",
    "## Transformation operations",
    "",
    table(
      ["Kind", "Title", "Group", "Required parameters", "Optional parameters"],
      operations.map((operation) => [
        code(operation.kind),
        escapeCell(operation.title),
        escapeCell(operation.group),
        parameterList(operation.required),
        parameterList(operation.optional)
      ])
    ),
    "",
    "## Runtime protocol v2",
    "",
    `Canonical schema: \`protocol/data-explorer.v2.schema.json\`. Protocol version: \`${schema.definitions.ProtocolVersion.enum.join(", ")}\`.`,
    "",
    table(
      ["Direction", "Schema type", "Message kind"],
      [
        ...messageKinds("DataExplorerRequest").map(({ name, kind }) => ["Request", code(name), code(kind)]),
        ...messageKinds("DataExplorerResponse").map(({ name, kind }) => ["Response", code(name), code(kind)])
      ]
    ),
    "",
    "## Notebook MIME types",
    "",
    ...packageJson.contributes.notebookRenderer.flatMap((renderer) =>
      renderer.mimeTypes.map((mimeType) => `- \`${mimeType}\``)
    ),
    ""
  ].join("\n")}\n`,
  { parser: "markdown" }
);

if (process.argv.includes("--check")) {
  const existing = readFileSync(outputPath, "utf8");
  if (existing !== output) {
    throw new Error("Generated interface reference is stale. Run npm run generate:reference.");
  }
} else {
  writeFileSync(outputPath, output, "utf8");
}

function loadOperationCatalog() {
  const candidates = [
    process.env.DATA_EXPLORER_PYTHON,
    process.platform === "win32"
      ? resolve(root, ".venv", "Scripts", "python.exe")
      : resolve(root, ".venv", "bin", "python"),
    "python3",
    "python"
  ].filter(Boolean);
  const command = [
    "-c",
    "import json; from data_wrangler_runtime.operations import operation_catalog; print(json.dumps(operation_catalog()))"
  ];
  for (const executable of candidates) {
    const isPath = executable.includes("/") || executable.includes("\\");
    if (isPath && !existsSync(executable)) continue;
    const result = spawnSync(executable, command, {
      cwd: root,
      encoding: "utf8",
      env: {
        ...process.env,
        PYTHONPATH: [resolve(root, "python"), process.env.PYTHONPATH].filter(Boolean).join(delimiter)
      }
    });
    if (result.status === 0) return JSON.parse(result.stdout);
  }
  throw new Error("Could not load the operation registry from a Python 3.10-3.14 interpreter.");
}

function messageKinds(unionName) {
  return schema.definitions[unionName].oneOf.map((entry) => {
    const name = entry.$ref.split("/").at(-1);
    const definition = schema.definitions[name];
    const object = definition.allOf?.find((part) => part.properties?.kind) ?? definition;
    return { name, kind: object.properties.kind.const };
  });
}

function table(headers, rows) {
  return [
    `| ${headers.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.join(" | ")} |`)
  ].join("\n");
}

function parameterList(parameters) {
  return parameters.length ? parameters.map(code).join(", ") : "—";
}

function code(value) {
  return `\`${String(value).replaceAll("`", "\\`")}\``;
}

function escapeCell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}
